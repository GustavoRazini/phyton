"""
Programação Orientada a Objetos - POO

- POO é um paradigma de programação que utiliza mapeamento de objetos do mundo real para modelos computacionas.

- Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas.

Principais elementos da Orientação a Objetos
- Classe -> Modelo do objeto do mundo real sendo apresentado computacionalmente;
- Objeto -> Instância da classe;
- Atributo -> Características do objeto;
- Método -> Comportamento do objeto (funções);
- Construtor -> Método especial utilizando para cirar os objetos;

"""

numero = 10

print(numero)
print(type(numero))

nome = 'Gustavo'

print(nome)
print(type(nome))


class Produto:
	pass


# ps4 -> objeto; Produto -> construtor
